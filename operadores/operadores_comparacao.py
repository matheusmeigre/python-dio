saldo = 400
saque = 200

print(saldo == saque) #Saldo é igual a saque?
print(saldo != saque) #Saldo é diferente ou igual a saque?
print(saldo > saque)  #Saldo é maior do que saque?
print(saldo >= saque) #Saldo é maior ou igual a saque?
print(saldo < saque)  #Saldo é menor do que saque?
print(saldo <= saque) #Saldo é menor ou igual a saque?
