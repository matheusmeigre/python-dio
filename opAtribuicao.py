#Sinal de atribuição ( = )
saldo = 500
print(saldo)

#Sinal de atribuição por adição ( += )
saldo = 500
saldo += 300
print(saldo)

#Sinal de atribuição por subtração ( -= )
saldo = 500
saldo -= 300
print(saldo)

#Sinal de atribuição por divisão ( /= )
saldo = 500
saldo /= 5
print(saldo)

saldo = 500
saldo //= 10
print(saldo)
#Sinal de atribuição por módulo ( %= )
saldo = 500
saldo %= 300
print(saldo)

#Sinal de atribuição por exponenciação ( **= )
saldo = 500
saldo **= 4
print(saldo)

